namespace solution {
  class Solver {
  public:
    const OutputStorage& solve( const InputStorage& in, OutputStorage& out, DataStorage& data ) {
      out.result = find_minimum_works(in.A, in.N);
      return out;
    }
    
  protected:
    static Int find_minimum_works( const Int A[SIZE], const int& N ) {
      Int res  = 0;
      Int sum1 = 0;
      Int sum2 = 0;
      for ( int i = 0; i < N - 1; ++ i ) {
        if ( A[i] > 0 ) {
          sum1 += std::abs(A[i]);
        } else {
          sum2 += std::abs(A[i]);
        }
        res += std::abs(sum1 - sum2);
      }
      return res;
    }
    
  };
}

